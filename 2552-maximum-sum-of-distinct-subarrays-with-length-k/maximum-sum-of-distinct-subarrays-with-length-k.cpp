class Solution {
public:
    long long maximumSubarraySum(vector<int>& nums, int k) {
        long long sum = 0;
        long long maxsum = 0;
        unordered_map<int, int> mp;

        for (int i = 0; i < nums.size(); i++) {

            mp[nums[i]]++;
            sum += nums[i];

            if (i >= k) {
                sum -= nums[i - k];
                mp[nums[i - k]]--;
                if (mp[nums[i - k]] == 0) {
                    mp.erase(nums[i - k]);
                }
            }

            if (i >= k - 1 && mp.size() == k) {
                maxsum = max(maxsum, sum);
            }
        }

        return maxsum;
    }
};
